class Ball {
    private ballXPostion: number;
    private ballYPostion: number;
    private ballSpeedX: number;
    private ballSpeedY: number;
    private ballDiameter: number;

    constructor(ballXPostion: number, ballYPostion: number, ballSpeedX: number, ballSpeedY: number, ballDiameter: number){
        this.ballXPostion = ballXPostion;
        this.ballYPostion = ballYPostion;
        this.ballSpeedX = ballSpeedX;
        this.ballSpeedY = ballSpeedY;
        this.ballDiameter = ballDiameter;
    }

}